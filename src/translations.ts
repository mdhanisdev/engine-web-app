export type LangType = 'EN' | 'FA';
export type CurrencyType = 'IRT' | 'USD';

// Helper to convert prices
export function formatPrice(amountIRT: number, currency: CurrencyType, lang: LangType): string {
  if (currency === 'USD') {
    // Arbitrary conversion rate: 1 USD = 60,000 IRT (Toman) for display accuracy
    const amountUSD = amountIRT / 60000;
    const value = amountUSD.toFixed(1);
    return lang === 'FA' 
      ? `${toPersianNumbers(value)} دلار` 
      : `$${value} USD`;
  } else {
    // IRT localized formatted
    const formatted = amountIRT.toLocaleString('en-US');
    return lang === 'FA' 
      ? `${toPersianNumbers(formatted)} تومان` 
      : `${formatted} IRT`;
  }
}

// Convert numbers to Persian characters if FA
export function toPersianNumbers(str: string | number): string {
  const farsiDigits = ['۰', '۱', '۲', '۳', '۴', '۵', '۶', '۷', '۸', '۹'];
  return str.toString().replace(/[0-9]/g, (w) => farsiDigits[parseInt(w, 10)]);
}
